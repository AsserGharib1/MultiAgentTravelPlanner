# ═══════════════════════════════════════════════════════════════════════
# Section (b)/(c) — Fine-Tuning Function with TensorBoard + Learning Curves
#
# For each epoch this function:
#   1. Trains one epoch with CosineSimilarityLoss via model.fit()
#   2. Evaluates val set → accuracy, precision, recall, F1
#   3. Approximates train loss on a 100-sample subset (cosine vs label)
#   4. Logs all scalars to TensorBoard SummaryWriter
#   5. Saves best-val-F1 checkpoint
# ═══════════════════════════════════════════════════════════════════════

import os, math, time
from torch.nn import MSELoss

def _approx_loss(model, examples, batch_size=32):
    """Compute mean CosineSimilarityLoss on a subset (no gradients)."""
    model.eval()
    criterion = MSELoss()
    losses_val = []
    subset = examples[:min(100, len(examples))]
    for i in range(0, len(subset), batch_size):
        batch = subset[i : i + batch_size]
        texts_a = [e.texts[0] for e in batch]
        texts_b = [e.texts[1] for e in batch]
        labels  = torch.tensor([e.label for e in batch], dtype=torch.float32).to(DEVICE)

        emb_a = model.encode(texts_a, convert_to_tensor=True, device=DEVICE)
        emb_b = model.encode(texts_b, convert_to_tensor=True, device=DEVICE)

        cos_sim = torch.nn.functional.cosine_similarity(emb_a, emb_b)
        loss    = criterion(cos_sim, labels)
        losses_val.append(loss.item())
    return float(np.mean(losses_val)) if losses_val else 1.0

def _eval_val(model, val_examples, threshold=0.5, temperature=1.0):
    """Predict labels on val set; return accuracy and F1."""
    if not val_examples:
        return 0.0, 0.0
    texts_a  = [e.texts[0] for e in val_examples]
    texts_b  = [e.texts[1] for e in val_examples]
    true_lbl = [int(e.label >= 0.5) for e in val_examples]

    emb_a = model.encode(texts_a, convert_to_tensor=True, device=DEVICE)
    emb_b = model.encode(texts_b, convert_to_tensor=True, device=DEVICE)
    sims  = torch.nn.functional.cosine_similarity(emb_a, emb_b)

    # Apply softmax temperature: scale logit then threshold
    scaled = (sims / temperature).sigmoid().cpu().numpy()
    pred   = (scaled >= threshold).astype(int).tolist()

    acc = accuracy_score(true_lbl, pred)
    f1  = f1_score(true_lbl, pred, zero_division=0)
    return float(acc), float(f1)


def train_model(config, train_examples, val_examples, save_root="/content/models"):
    """
    Fine-tune a SentenceTransformer config for N epochs.

    Returns:
        best_model  — SentenceTransformer at best val-F1 checkpoint
        history     — dict of lists: train_loss, val_acc, val_f1 per epoch
    """
    run_name = config["name"].replace(" ", "_").replace("—", "").strip("_")
    log_dir  = f"/content/runs/{run_name}"
    save_dir = f"{save_root}/{run_name}"
    os.makedirs(save_dir, exist_ok=True)

    writer = SummaryWriter(log_dir=log_dir)

    # ── Load fresh model for this config ──────────────────────────────
    model = SentenceTransformer(config["model_name"])
    model.max_seq_length = config["max_seq_length"]

    train_loss_fn = losses.CosineSimilarityLoss(model)
    train_loader  = DataLoader(train_examples, shuffle=True,
                               batch_size=config["batch_size"])

    history = {"train_loss": [], "val_acc": [], "val_f1": []}
    best_f1   = -1.0
    best_model = None

    print(f"\n{'─'*60}")
    print(f"Training: {config['name']}")
    print(f"  lr={config['learning_rate']} | epochs={config['epochs']} | "
          f"batch={config['batch_size']} | temp={config['temperature']}")
    print(f"  log_dir: {log_dir}")
    print(f"{'─'*60}")

    for epoch in range(1, config["epochs"] + 1):
        t0 = time.time()

        # ── Train one epoch ───────────────────────────────────────────
        model.fit(
            train_objectives=[(train_loader, train_loss_fn)],
            epochs=1,
            warmup_steps=config["warmup_steps"] if epoch == 1 else 0,
            optimizer_params={"lr": config["learning_rate"]},
            weight_decay=config["weight_decay"],
            show_progress_bar=False,
        )

        # ── Compute metrics ──────────────────────────────────────────
        t_loss = _approx_loss(model, train_examples)
        v_acc, v_f1 = _eval_val(model, val_examples,
                                 temperature=config["temperature"])

        elapsed = time.time() - t0
        history["train_loss"].append(t_loss)
        history["val_acc"].append(v_acc)
        history["val_f1"].append(v_f1)

        # ── TensorBoard logging ──────────────────────────────────────
        writer.add_scalar("Loss/Train",       t_loss, epoch)
        writer.add_scalar("Metrics/Accuracy", v_acc,  epoch)
        writer.add_scalar("Metrics/F1",       v_f1,   epoch)
        writer.flush()

        print(f"  Epoch {epoch}/{config['epochs']} | "
              f"loss={t_loss:.4f}  acc={v_acc:.3f}  F1={v_f1:.3f}  "
              f"({elapsed:.1f}s)")

        # ── Save best checkpoint ─────────────────────────────────────
        if v_f1 >= best_f1:
            best_f1   = v_f1
            best_model = model
            model.save(save_dir)

    writer.close()
    print(f"\n  Best F1={best_f1:.3f}  →  saved to {save_dir}")
    return best_model, history


