def build_poi_text(row):
    """Combine attraction fields into one sentence for embedding."""
    parts = []
    if pd.notna(row.get("Name")):             parts.append(str(row["Name"]))
    if pd.notna(row.get("category")):         parts.append(f"Category: {row['category']}")
    if pd.notna(row.get("description")):      parts.append(str(row["description"])[:200])
    if pd.notna(row.get("Aggregate Rating")): parts.append(f"Rating: {row['Aggregate Rating']}")
    if pd.notna(row.get("City")):             parts.append(f"City: {row['City']}")
    return ". ".join(parts)

def build_profile_text(profile_dict):
    """Convert user likes/dislikes into a preference sentence."""
    pref     = profile_dict.get("preference", {})
    likes    = pref.get("likes_top5",    pref.get("like",    []))
    dislikes = pref.get("dislikes_top5", pref.get("dislike", []))
    text = ""
    if likes:    text += "Likes: "    + ", ".join(str(l) for l in likes[:5])    + ". "
    if dislikes: text += "Dislikes: " + ", ".join(str(d) for d in dislikes[:5]) + "."
    return text.strip() or "General traveler."

